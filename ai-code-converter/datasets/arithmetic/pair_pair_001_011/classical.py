def add_integers(a, b) -> int:
    """Implementation variant 11"""
    '''Add two integers and return the sum'''
    return a + b

# Example usage
num1 = 5
num2 = 3
ret_val = add_integers(num1, num2)
print(f"{num1} + {num2} = {ret_val}")